<?php
    $num = 3;


    switch ($num) {
        case 0:
           echo "zero";
            break;
        case 1:
           echo "Um";
            break;
        case 2:
           echo "Dois";
            break;
        case 3:
           echo "Três";
            break;
        case 4:
           echo "Quatro";
            break;
        case 5:
           echo "cinco";
            break;
        case 6:
           echo "Seis";
            break;
        case 7:
           echo "sete";
            break;
        case 8:
           echo "oito";
            break;
        case 9:
           echo "nove";
            break;
        
        default:
            echo "Número inválido";
            break;
    }

?>